    var $language = 'en';

    /**
     *  A common used technique is, to keep a default instance
     *  for shared classes. In this case, the global variable
     *  $Language has become a member variable of this class,
     *  which is more consistent from the OO point of view.
     *
     *  Calling this method will return the default instance.
     *  If the default instance does not exist, it will be
     *  created.
     *
     *  One defect of PHP is, that it does not allow static
     *  class member variables - thus we have to use a common
     *  global to hold the default instance ;-)
     */
    function & GetDefaultInstance() {
        global $__default_trans_instance;

        if (!$__default_trans_instance) {
            $__default_trans_instance = new Trans();
        }

        return $__default_trans_instance;
    }

    /**
     *  The translate method takes a string $msg and three
     *  optional positional parameters ($p1,$p2,$p3). The
     *  positional parameters will substitute occurrencies
     *  of the form ^1, ^2 and ^3 contained in the string.
     *
     *  Positional parameters are useful, to include parameters
     *  like filenames or others. In different languages, it is
     *  likely, that the position changes:
     *
     *    'the _white_ dog'   vs.   'o cachorro _branco_'
     */
    function Translate($msg, $p1=null, $p2=null, $p3=null) {
        $inst = & Trans::GetDefaultInstance();

        //
        // $nls will become our localized message, if one would
        // be found. Otherwise, it will become the original message
        // prepended by an underscore to notify, that a localized
        // message is missing.
        //

        $nls = $msg;

        if ($inst->language != 'en') {
            $nls = $msg;

            if (is_array($inst->messages[$inst->language])) {
                $i = 0;

                foreach ($inst->messages['en'] as $m) {
                    if ($m == $msg) {
                        $m = $inst->messages[$inst->language][$i];

                        if ($m) {
                            $nls = $m;
                            break;
                        }
                    }

                    $i++;
                }
            }
        }

        //
        // substitute positional parameters if necessary
        //

        if ($p1 != null)
            $nls = str_replace('^1', $p1, $nls);

        if ($p2 != null)
            $nls = str_replace('^2', $p2, $nls);

        if ($p3 != null)
            $nls = str_replace('^3', $p3, $nls);

        return $nls;
    }

    /*     * ******************************************************** */
    /* returns the currently selected language identifier
      /********************************************************** */

    function GetLanguage() {
        $inst = & Trans::GetDefaultInstance();

        return $inst->language;
    }

    /*     * ******************************************************** */
    /* switches to another language. If the specified language
      /* is not defined, an error message is generated
      /********************************************************** */

    function SetLanguage($lang, $showMsg = true) {
        $inst = & Trans::GetDefaultInstance();

        if (!$inst->messages[$lang]) {
            if ($showMsg) {
                new Dialog("Language '$lang' is not supported!");
            }
            return false;
        }

        $inst->language = $lang;
        return true;
    }

