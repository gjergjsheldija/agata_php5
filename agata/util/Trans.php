    /* for testing and demonstrating the new features
      /********************************************************** */

    function Dump() {
        $inst = & Trans::GetDefaultInstance();

        $old_lang = Trans::GetLanguage();

        // dumps mapping of all available messages except the default ones
        foreach (array_keys($inst->messages) as $l) {
            if ($l == 'en')
                continue;

            $inst->language = $l;

            echo "<table bgcolor=#999999 cellspacing=1 cellpadding=2 width=100%>\n";
            echo "<tr><td colspan=3 align=center><font color=white><b>Current language is '$l'</b></font></td></tr>\n";

            foreach ($inst->messages['en'] as $m)
                echo "<tr><td width=50% bgcolor=#efefef><i>$m</i></td>"
                . "<td align=center bgcolor=#ffefef>&nbsp;=&gt;&nbsp;</td>"
                . "<td width=50% bgcolor=#ffffef>" . Trans::Translate($m) . "</td></tr>\n";

            echo "</table>\n";
            echo "<br>\n";
        }

        Trans::SetLanguage($old_lang);

        //
        // positional parameters test
        //

        $samples = array(
            array('The numbers are ^1, ^2 and ^3', array('one', 'two', 'three')),
            array('Little boys like ^2 and little girls like ^1 ...', array('dolls', 'cars')),
            array('... and when they become older, ^1 like ^2 and ^2 like ^1', array('boys', 'girls'))
        );

        echo "<table bgcolor=#999999 cellspacing=1 cellpadding=2 width=100%>\n";
        echo "<tr><td colspan=3 align=center><font color=white><b>Postional Parameters</b></font></td></tr>\n";

        foreach ($samples as $s) {
            $m = $s[0];

            list ( $p1, $p2, $p3 ) = $s[1];

            echo "<tr><td width=50% bgcolor=#efefef><i>$m</i></td>"
            . "<td align=center bgcolor=#ffefef>&nbsp;=&gt;&nbsp;</td>"
